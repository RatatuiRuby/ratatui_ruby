module Autodoc
  class Name < Data.define(:string)
    def snake
      string.to_s
        .gsub(/([A-Z]+)([A-Z][a-z])/, '\1_\2')
        .gsub(/([a-z\d])([A-Z])/, '\1_\2')
        .downcase
    end

    def to_s
      string.to_s
    end
  end
end
